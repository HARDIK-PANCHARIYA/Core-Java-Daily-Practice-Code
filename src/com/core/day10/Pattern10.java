public class Pattern10 {
	
	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++) {
			
			for(int j=1;j<=3;j++) {
				System.out.print("#");
			}
			
			for(int k=4;k<=6;k++) {
			System.out.print("@");
			}
			
			for(int q=7;q<=9;q++) {
				System.out.print("*");
			}
			
		System.out.println();
		}
		
	}

}
