public class Pattern9 {
	
	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++) {
			
			for (int j=1;j<=2;j++) {
				
				System.out.print("#");
			}
			
			for(int k=3;k<=4;k++) {
				System.out.print("@");
			}
			
			for(int q=5;q<=5;q++) {
				System.out.print("*");
			}
			
			System.out.println();
			
		}
	}

}
