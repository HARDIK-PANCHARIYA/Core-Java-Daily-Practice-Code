package com.core.day5;

public class Datatype {
	
	public static void main (String[] args){
		
		System.out.println("-----byte (8 bit) -- Range  (-128 to 127)---");
		byte b = 126;
		System.out.println(b+"\n");
		
		System.out.println("----short (16 bit )--Range (-32,768 to 32,767)----");
		short s=30000;
		System.out.println(s+"\n");
		
		System.out.println("----int (32 bit)---Range (-2,147,483,648 to 2,147,483,647)----");
		int i= 2000554;
		System.out.println(i+"\n");
		
		System.out.println("---long (64 bit)--Range (-9,223,372,036,854,775,808 to 9,223,372,036,854,775,807)---");
		System.out.println("IMP--- Always put (l) at the end of long");
		long l=93684784837598574l;
		System.out.println(l+"\n");
		
		System.out.println("---float (32 bit)--Range (6-7 significant decimal digits)-----");
		System.out.println("IMP----- Always out (f) at the end of float");
		float f=3.7656387f;
		System.out.println(f+"\n");
		
		System.out.println("----double (64 bit)---Range (15 significant decimal digits)-----");
		double d=11.11937897478959;
		System.out.println(d+"\n");
		
		System.out.println("----char (16 bit)-----");
		char c='@';
		System.out.println(c+"\n");
		
		System.out.println("------String------------");
		String S="Hardik Panchariya";
		System.out.println(S+"\n");
		
		
		System.out.println("---boolean---- (true or false)-----");
		boolean bl=true;
		System.out.println(bl+"\n");
				
		
		
			
	}

}
